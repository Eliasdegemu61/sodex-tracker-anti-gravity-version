export function getTokenLogo(pair: string): string | undefined {
  // Extract the token symbol from pair format
  // Handles both "BTC/USDC" and "BTC-USD" formats
  // Returns the first part as the token symbol
  const symbol = pair.split(/[\/\-]/)[0].toUpperCase()
  return TOKEN_LOGOS[symbol]
}
